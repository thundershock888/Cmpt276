package champs;

public class Image{
	private int W;
	private String sprite;
	private int X;
	private int H;
	private int Y;
	private String full;
	private String group;

	public int getW(){
		return W;
	}

	public String getSprite(){
		return sprite;
	}

	public int getX(){
		return X;
	}

	public int getH(){
		return H;
	}

	public int getY(){
		return Y;
	}

	public String getFull(){
		return full;
	}

	public String getGroup(){
		return group;
	}
}
