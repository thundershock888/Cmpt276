package champs;

public class SkinsItem{
	private boolean chromas;
	private int num;
	private String name;
	private String id;

	public boolean isChromas(){
		return chromas;
	}

	public int getNum(){
		return num;
	}

	public String getName(){
		return name;
	}

	public String getId(){
		return id;
	}
}
